export function getBrFilterHint(option: BrFilterOption, lang: Lang): string | undefined {
  return lang === "es" ? option.hintEs : option.hintEn;
}

export function getBrDrawerSectionLabel(section: BrDrawerSection, lang: Lang): string {
  const labels: Record<BrDrawerSection, { es: string; en: string }> = {
    queBuscas: { es: "¿Qué buscas?", en: "What are you looking for?" },
    donde: { es: "¿Dónde?", en: "Where?" },
    presupuesto: { es: "¿Presupuesto?", en: "Budget?" },
    tamano: { es: "¿Tamaño?", en: "Size?" },
    caracteristicas: { es: "¿Características?", en: "Features?" },
    condicion: { es: "¿Condición y disponibilidad?", en: "Condition & availability?" },
    terreno: { es: "¿Terreno?", en: "Land?" },
    comercial: { es: "¿Comercial / inversión?", en: "Commercial / investment?" },
    medios: { es: "Medios", en: "Media" },
    quienPublica: { es: "¿Quién publica?", en: "Who publishes?" },
  };
  return lang === "es" ? labels[section].es : labels[section].en;
}
