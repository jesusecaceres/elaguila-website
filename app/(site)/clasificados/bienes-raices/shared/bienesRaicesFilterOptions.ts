/**
 * Bienes Raíces — Shared filter option registry.
 *
 * Single source of truth for landing chips, results chips, and drawer options.
 * All three surfaces pull from this config to ensure label/value consistency.
 *
 * Visibility flags:
 * - landingFeatured: Shown on landing as intent tiles or shortcut chips
 * - resultsChip: Shown on results as fast filter chips
 * - drawerFull: Shown in drawer as full expanded menu
 *
 * Support status:
 * - supported: Data exists in listing rows and filter function handles it
 * - deferred: Data exists in application but not yet wired to filter function
 * - unavailable: Data not available in public listing results
 */

import type { Lang } from "@/app/clasificados/config/clasificadosHub";

// ─── Filter option types ───────────────────────────────────────────────────────

export type BrFilterVisibility = "landingFeatured" | "resultsChip" | "drawerFull";
export type BrFilterSupport = "supported" | "deferred" | "unavailable";

export interface BrFilterOption {
  /** Unique identifier for the option */
  id: string;
  /** Human-readable label (ES) */
  labelEs: string;
  /** Human-readable label (EN) */
  labelEn: string;
  /** URL query parameter key */
  queryKey: string;
  /** Value to set in URL when selected */
  queryValue: string;
  /** Application source field(s) */
  appSourceFields: string[];
  /** Where this option appears */
  visibility: BrFilterVisibility[];
  /** Current support status */
  support: BrFilterSupport;
  /** Section in drawer */
  drawerSection: BrDrawerSection;
  /** Optional hint/helper text */
  hintEs?: string;
  hintEn?: string;
}

