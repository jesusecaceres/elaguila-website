type WidenCopy<T> =
  T extends (...args: infer Args) => infer Return
    ? (...args: Args) => Return
    : T extends readonly (infer Item)[]
      ? readonly WidenCopy<Item>[]
      : T extends string
        ? string
        : T extends object
          ? { readonly [K in keyof T]: WidenCopy<T[K]> }
        : T;

